package com.dhuyo.display.university.data;

public class UniversityInfo {
	
	private String studentData;
	private String teacherData;
	private String subjectData;
	
	public String getStudentData() {
		return studentData;
	}

	public String getTeacherData() {
		return teacherData;
	}
	
	public String getSubjectData() {
		return subjectData;
		
	}
	
	public void setStudentData(String studentData) {
		this.studentData = studentData;
		
	}
	
	public void setTeacherData(String teacherData) {
		this.teacherData = teacherData;
		
	}
	
	public void setSubjectData(String subjectData) {
		this.subjectData = subjectData;
		
	}
	
}
