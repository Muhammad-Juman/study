package com.dhuyo.calculator.scientific;

public class Scientific extends Operators {
	public void addition() {
		System.out.println("Addition : 5 + 5 = " + (5 + 5));
	}

	public void subtraction() {
		System.out.println("Subtraction : 6 - 2 =  " + (6 - 2));
	}

	public void multiplication() {
		System.out.println("Multiplication : 6 * 5 =  " + (6 * 5));
	}

	public void division() {
		System.out.println("Division : 5 / 25 = " + (5 / 25));
	}

	public void reminder() {
		System.out.println("Reminder : 27 % 7 = " + (27 % 7));
	}

	public void sin() {
		System.out.println("Sin 1 = 0.8414709848 ");
	}

	public void cos() {
		System.out.println("Cos 1 = 0.54030230586");
	}

	public void tan() {
		System.out.println("Tan 1 = 1.55740772465");
	}

}
