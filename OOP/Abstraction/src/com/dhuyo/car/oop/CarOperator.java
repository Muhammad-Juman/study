package com.dhuyo.car.oop;

public abstract class CarOperator {
	// Abstract Getter Methods
	protected abstract String getName();

	protected abstract int getNumber();

	protected abstract int getTyers();

	protected abstract String getHonk();

	protected abstract String getUse();

	protected abstract int getSpeed();

	protected abstract String getBody();

	// Abstract Setter Methods
	protected abstract void setName(String name);

	protected abstract void setNumber(int number);

	protected abstract void setTyers(int tyers);

	protected abstract void setHonk(String honk);

	protected abstract void setUse(String use);

	protected abstract void setSpeed(int speed);

	protected abstract void setBody(String body);

}
