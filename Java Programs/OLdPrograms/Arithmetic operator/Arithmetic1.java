public class Arithmetic1{
	public static void main(String[] args){
		
			int number1=55; number2=22; number3=60;
				
		

			System.out.println("number1 + number2 / number3::");
			
			System.out.println(number1 + number2 / number3);
			
			System.out.println("number1 - number2 * number3::");
			
			System.out.println(number1 - number2 * number3);
			
			System.out.println("number1 * number2 - number3::");
			
			System.out.println(number1 * number2 - number3);
			
			System.out.println("number1 / number2 + number3::");
			
			System.out.println(number1 / number2 + number3);

			System.out.println("number1 % number2 + number3::");
			
			System.out.println(number1 % number2 + number3);
	}
}